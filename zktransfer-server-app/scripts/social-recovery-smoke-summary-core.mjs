export function buildSummaryPayload({
  baseDir,
  overview,
  latestDir,
  snapshotsDir,
  historyText,
}) {
  return {
    baseDir,
    overview,
    paths: {
      latestDir,
      latestReportPath: `${latestDir}/report.md`,
      latestOverviewPath: `${latestDir}/overview.md`,
      latestComparePath: `${latestDir}/compare.txt`,
      latestChecksumsComparePath: `${latestDir}/checksums-compare.txt`,
      latestRegressionsPath: `${latestDir}/regressions.txt`,
      latestRegressionsHistoryPath: `${latestDir}/regressions-history.txt`,
      latestRegressionsHistoryGateFailingPath: `${latestDir}/regressions-history-gate-failing.txt`,
      latestRegressionsHistoryGateFailingComparePath: `${latestDir}/regressions-history-gate-failing-compare.txt`,
      latestRegressionsHistoryGateFailingCompareCheckPath: `${latestDir}/regressions-history-gate-failing-compare-check.txt`,
      latestRegressionsHistoryComparePath: `${latestDir}/regressions-history-compare.txt`,
      latestRegressionsHistoryCompareCheckPath: `${latestDir}/regressions-history-compare-check.txt`,
      latestRegressionsComparePath: `${latestDir}/regressions-compare.txt`,
      latestRegressionsChangesPath: `${latestDir}/regressions-changes.txt`,
      latestRegressionsStatusPath: `${latestDir}/regressions-status.txt`,
      latestRegressionsOverviewPath: `${latestDir}/regressions-overview.md`,
      latestRegressionsPlanPath: `${latestDir}/regressions-plan.txt`,
      latestRegressionsNextPath: `${latestDir}/regressions-next.txt`,
      latestRegressionsSummaryPath: `${latestDir}/regressions-summary.md`,
      latestRegressionsDoctorPath: `${latestDir}/regressions-doctor.txt`,
      latestRegressionsReportPath: `${latestDir}/regressions-report.md`,
      latestRegressionsIntegrityPath: `${latestDir}/regressions-integrity.txt`,
      latestNextPath: `${latestDir}/next.txt`,
      historyPath: `${baseDir}/history.md`,
      indexPath: `${snapshotsDir}/index.json`,
    },
    historyPreview: historyText
      ? historyText.split("\n").slice(0, 8).join("\n").trim()
      : null,
  };
}

export function renderSummaryMarkdown(payload, renderOverviewMarkdown) {
  const lines = [
    "# Social Recovery Smoke Summary",
    "",
    renderOverviewMarkdown(payload.overview).trimEnd(),
    "",
    "## Paths",
    "",
    `- latestDir: ${payload.paths.latestDir}`,
    `- latestReportPath: ${payload.paths.latestReportPath}`,
    `- latestOverviewPath: ${payload.paths.latestOverviewPath}`,
    `- latestComparePath: ${payload.paths.latestComparePath}`,
    `- latestChecksumsComparePath: ${payload.paths.latestChecksumsComparePath}`,
    `- latestRegressionsPath: ${payload.paths.latestRegressionsPath}`,
    `- latestRegressionsHistoryPath: ${payload.paths.latestRegressionsHistoryPath}`,
    `- latestRegressionsHistoryGateFailingPath: ${payload.paths.latestRegressionsHistoryGateFailingPath}`,
    `- latestRegressionsHistoryGateFailingComparePath: ${payload.paths.latestRegressionsHistoryGateFailingComparePath}`,
    `- latestRegressionsHistoryGateFailingCompareCheckPath: ${payload.paths.latestRegressionsHistoryGateFailingCompareCheckPath}`,
    `- latestRegressionsHistoryComparePath: ${payload.paths.latestRegressionsHistoryComparePath}`,
    `- latestRegressionsHistoryCompareCheckPath: ${payload.paths.latestRegressionsHistoryCompareCheckPath}`,
    `- latestRegressionsComparePath: ${payload.paths.latestRegressionsComparePath}`,
    `- latestRegressionsChangesPath: ${payload.paths.latestRegressionsChangesPath}`,
    `- latestRegressionsStatusPath: ${payload.paths.latestRegressionsStatusPath}`,
    `- latestRegressionsOverviewPath: ${payload.paths.latestRegressionsOverviewPath}`,
    `- latestRegressionsPlanPath: ${payload.paths.latestRegressionsPlanPath}`,
    `- latestRegressionsNextPath: ${payload.paths.latestRegressionsNextPath}`,
    `- latestRegressionsSummaryPath: ${payload.paths.latestRegressionsSummaryPath}`,
    `- latestRegressionsDoctorPath: ${payload.paths.latestRegressionsDoctorPath}`,
    `- latestRegressionsReportPath: ${payload.paths.latestRegressionsReportPath}`,
    `- latestRegressionsIntegrityPath: ${payload.paths.latestRegressionsIntegrityPath}`,
    `- latestNextPath: ${payload.paths.latestNextPath}`,
    `- historyPath: ${payload.paths.historyPath}`,
    `- indexPath: ${payload.paths.indexPath}`,
    "",
    "## History Preview",
    "",
    payload.historyPreview || "(history not available)",
    "",
  ];

  return lines.join("\n");
}
